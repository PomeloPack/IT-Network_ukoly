smajlík = str(input("Zadej smajlíka: "))
if smajlík == ":)":
    print("Tvůj smajlík je veselý")
elif smajlík == ":(":
    print("Tvůj smajlík je smutný")
elif smajlík == ":P":
    print("Tvůj smajlík je filuta")
elif smajlík == ":*":
    print("Tvůj smajlík ti posílá pusinku")
else:
    print("Tohoto smajlíka neznám")
